-- {{{ Signals
-- Signal function to execute when a new client appears.
client.connect_signal("manage", function (c)
    -- Set the windows at the slave,
    -- NOTE: when composing placement functions, the last function has the highest
    -- Priority. IE if no_overlap is last a window will never overlap, but if
    -- no_offscreen is last a window will never be offscreen
    -- i.e. put it at the end of others instead of setting it master.
    if not awesome.startup 
        and not awful.placement.restore(c) then 
        awful.client.setslave(c) 
    end

    if awesome.startup 
        and not awful.placement.restore(c) then
        -- Prevent clients from being unreachable after screen count changes.
        local focus = (awful.placement.left +
                        awful.placement.no_overlap +
                        awful.placement.no_offscreen)
        focus(c)
    end
    -- round the corners if smoothing was not requested (edges may be rough)
    if (not beautiful.smooth_corners) then
        c.shape = function (cr,w,h) 
                      return gears.shape.rounded_rect(cr,w,h,beautiful.corner_radius) 
                  end
    end
end)

client.connect_signal("request::activate", function(client,context,hints)
    gears.table.crush(hints,{switch_to_tag = true})
    awful.ewmh.activate(client,context,hints)
end)

-- Add a titlebar if titlebars_enabled is set to true in the rules.
client.connect_signal("request::titlebars", titlebar.create_titlebar)

-- Enable sloppy focus, so that focus follows mouse.
client.connect_signal("mouse::enter", function(c)
    --disable sloppy focus in floating mode
    if awful.layout.get(c.screen) ~= awful.layout.suit.floating then
        c:emit_signal("request::activate", "mouse_enter", {raise = false})
    end
end)

client.connect_signal("focus", function(c) 
    c.border_color = beautiful.border_focus 
    titlebar.focus_titlebar(c)
    end)
client.connect_signal("unfocus", function(c) 
    c.border_color = beautiful.border_normal 
    titlebar.unfocus_titlebar(c)
end)
